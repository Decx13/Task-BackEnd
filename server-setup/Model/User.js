const { Sequelize, DataTypes } = require('sequelize');
const sequelize = new Sequelize('backend_task', 'root', '1234', {
  host: 'localhost',
  dialect: 'mysql'
});

const User = sequelize.define('User', {
  username: {
    type: DataTypes.STRING,
    allowNull: false
  },
  password: {
    type: DataTypes.STRING,
    allowNull: false
  }
});

module.exports = User;